int		ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	while (sqrt * sqrt < nb)
	{
		if (sqrt >= 46341)
			return (0);
		sqrt = sqrt + 1;
	}
	if (sqrt * sqrt == nb)
		return (sqrt);
	return (0);
}
